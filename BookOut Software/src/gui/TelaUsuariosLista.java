    private void btListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btListarActionPerformed
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.setColumnIdentifiers(new String[]{"CPF","NOME"});
        
        RepositorioUsuario rep = RepositorioUsuario.pegarInstancia();
        this.lista_usuarios = rep.listarUsuarios();
        
        for (int i = 0; i < lista_usuarios.size(); i++){
            modelo.addRow(new Object[]{this.lista_usuarios.get(i).getCpf(),
                this.lista_usuarios.get(i).getNome()});
        }
        
        jTable1.setModel(modelo);
        

        
    }//GEN-LAST:event_btListarActionPerformed

